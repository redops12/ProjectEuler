class Grid {
public:
    array<array<int, 9>, 9> sudoku;

    bool solve() {
        int i, j;
        bool zero_space = false;
        for (i = 0; i < 9; i++) {
            for (j = 0; j < 9; j++) {
                if (sudoku[i][j] == -1) {
                    zero_space = true;
                    break;
                }
            }
            if (zero_space) {
                break;
            }
        }
        if (!zero_space) return true;
        int top_left_i = i - i % 3;
        int top_left_j = j - j % 3;
        for (size_t k = 0; k < 9; k++) {
            static array<pair<int,int>, 9> offsets = {make_pair(0,0), make_pair(0,1), make_pair(0,2), make_pair(1,0), make_pair(1,1), make_pair(1,2), make_pair(2,0), make_pair(2,1), make_pair(2,2)};
            bool bad = false;
            for (int m = 0; m < 9; m++) {
                if (
                        sudoku[i][m] == k ||
                        sudoku[m][j] == k ||
                        sudoku[top_left_i + offsets[m].first][top_left_j + offsets[m].second] == k
                   ) {
                    bad = true;
                    break;
                }
            }
            if (bad) continue;

            Grid copy = *this;
            copy.sudoku[i][j] = k;
            if (copy.solve()) {
                this->sudoku = copy.sudoku;
                return true;
            }
        }
        return false;
    }

private:
};

int main (int argc, char *argv[]) {
    Grid g;
    int sum = 0;
    for (int gid = 0; gid < 50; gid++) {
        for (int i = 0; i < 9; i++) {
            for (int j = 0; j < 9; j++) {
                g.sudoku[i][j] = arr[gid][i][j] - 1;
            }
        }
        g.solve();
        sum += g.sudoku[0][0] * 100 + g.sudoku[0][1] * 10 + g.sudoku[0][2];
    }
    cout << sum << endl;

    return 0;
}
